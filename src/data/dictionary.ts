export const getAlphabeticalSections = () => {
  const sections: { [key: string]: DictionaryEntry[] } = {};
  
  sortedDictionaryData.forEach(entry => {
    const firstLetter = entry.term.charAt(0).toUpperCase();
    if (!sections[firstLetter]) {
      sections[firstLetter] = [];
    }
    sections[firstLetter].push(entry);
  });
  
  return sections;
};